class SkinConcerns:
    """Handles the user's skin concerns"""

    def __init__(self, wrinkles, acne, pores, pigmentation,
                 texture, irritation, dehydration, oil):
        self.wrinkles = wrinkles
        self.acne = acne
        self.pores = pores
        self.pigmentation = pigmentation
        self.texture = texture
        self.irritation = irritation
        self.dehydration = dehydration
        self.oil = oil
